package dao;

public class Wafer {
	private int id;
	private String meshName;
	private double poleHeight;
	private int basecoat;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the meshName
	 */
	public String getMeshName() {
		return meshName;
	}
	/**
	 * @param meshName the meshName to set
	 */
	public void setMeshName(String meshName) {
		this.meshName = meshName;
	}
	/**
	 * @return the poleHeight
	 */
	public double getPoleHeight() {
		return poleHeight;
	}
	/**
	 * @param poleHeight the poleHeight to set
	 */
	public void setPoleHeight(double poleHeight) {
		this.poleHeight = poleHeight;
	}
	/**
	 * @return the basecoat
	 */
	public int getBasecoat() {
		return basecoat;
	}
	/**
	 * @param basecoat the basecoat to set
	 */
	public void setBasecoat(int basecoat) {
		this.basecoat = basecoat;
	}
	
	
}
